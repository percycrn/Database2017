package client.data;

public class Insurance {
    private String insurance_no;
    private String insuranceCompany_name;
    private String insurance_name;
    private String insurance_description;

    public String getInsurance_no() {
        return insurance_no;
    }

    public void setInsurance_no(String insurance_no) {
        this.insurance_no = insurance_no;
    }

    public String getInsuranceCompany_name() {
        return insuranceCompany_name;
    }

    public void setInsuranceCompany_name(String insuranceCompany_name) {
        this.insuranceCompany_name = insuranceCompany_name;
    }

    public String getInsurance_name() {
        return insurance_name;
    }

    public void setInsurance_name(String insurance_name) {
        this.insurance_name = insurance_name;
    }

    public String getInsurance_description() {
        return insurance_description;
    }

    public void setInsurance_description(String insurance_description) {
        this.insurance_description = insurance_description;
    }
}
