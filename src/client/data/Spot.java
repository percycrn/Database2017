package client.data;

public class Spot {
    int spot_no;
    String spot_name;

    public int getSpot_no() {
        return spot_no;
    }

    public void setSpot_no(int spot_no) {
        this.spot_no = spot_no;
    }

    public String getSpot_name() {
        return spot_name;
    }

    public void setSpot_name(String spot_name) {
        this.spot_name = spot_name;
    }
}
